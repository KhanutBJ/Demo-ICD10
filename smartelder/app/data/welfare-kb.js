export function retrieveRelevant(query, topK = 3) {
  const q = query.toLowerCase();
  const scored = WELFARE_KB.map(doc => {
    const kwScore  = doc.keywords.reduce((acc, kw) => acc + (q.includes(kw.toLowerCase()) ? 2 : 0), 0);
    const words    = q.split(/\s+/).filter(w => w.length > 1);
    const wdScore  = words.reduce((acc, w) =>
      acc + (doc.content.toLowerCase().includes(w) || doc.title.toLowerCase().includes(w) ? 0.4 : 0), 0);
    return { doc, score: kwScore + wdScore };
  });
  return scored
    .filter(s => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, topK)
    .map(s => s.doc);
}
